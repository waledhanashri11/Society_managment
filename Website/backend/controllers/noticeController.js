const closePoll = async (req, res) => {
  try {
    await ensurePollSchema();
    const { id } = req.params;
    const [polls] = await promisePool.query('SELECT p.*, n.title FROM notice_polls p JOIN notices n ON n.id = p.notice_id WHERE n.id = ?', [id]);
    if (!polls.length) return res.status(404).json({ message: 'Poll not found' });
    await promisePool.query('UPDATE notice_polls SET closed_at = NOW(), updated_at = NOW() WHERE id = ?', [polls[0].id]);
    await notifyResidents(`Poll ended: ${polls[0].title}`, 'Poll results are now available in the notice board.', id);
    res.json({ message: 'Poll closed successfully' });
  } catch (error) {
    console.error('Close poll error:', error);
    res.status(500).json({ message: 'Server error' });
  }
};

const castNoticeVote = async (req, res) => {
  const connection = await promisePool.getConnection();
  try {
    await ensurePollSchema();
    if (req.user.role !== 'resident') return res.status(403).json({ message: 'Only residents can vote in notice polls' });
    const { id } = req.params;
    const selectedOptionIds = asArray(req.body.option_ids || req.body.optionIds).map(Number).filter(Boolean);
    const [notices] = await connection.query('SELECT * FROM notices WHERE id = ?', [id]);
    if (!notices.length) return res.status(404).json({ message: 'Notice not found' });

    const notice = await attachPolls(notices[0], req.user);
    const poll = notice.poll;
    if (!poll) return res.status(400).json({ message: 'This notice does not have a poll' });
    if (pollStatus(poll) !== 'Poll Active') return res.status(400).json({ message: 'Voting is not active for this poll' });
    if (!selectedOptionIds.length) return res.status(400).json({ message: 'Select at least one option' });
    if (poll.poll_type !== 'multiple_choice' && selectedOptionIds.length > 1) return res.status(400).json({ message: 'Only one option can be selected' });

    const validIds = new Set(poll.options.map((option) => Number(option.id)));
    if (selectedOptionIds.some((optionId) => !validIds.has(optionId))) return res.status(400).json({ message: 'Invalid poll option selected' });

    const [existing] = await connection.query('SELECT id FROM notice_poll_votes WHERE poll_id = ? AND resident_id = ?', [poll.id, req.user.id]);
    if (existing.length && !poll.allow_vote_change) return res.status(409).json({ message: 'You have already voted in this poll' });

    const [userRows] = await connection.query(
      `SELECT COALESCE(f.flat_no, '') AS flat_no
       FROM users u
       LEFT JOIN flats f ON f.id = u.flat_id
       WHERE u.id = ?`,
      [req.user.id]
    );
    const flatNo = userRows[0]?.flat_no || null;

    await connection.beginTransaction();
    if (existing.length) {
      await connection.query('DELETE FROM notice_poll_votes WHERE poll_id = ? AND resident_id = ?', [poll.id, req.user.id]);
    }
    for (const optionId of selectedOptionIds) {
      await connection.query(
        'INSERT INTO notice_poll_votes (poll_id, option_id, resident_id, flat_no) VALUES (?, ?, ?, ?)',
        [poll.id, optionId, req.user.id, flatNo]
      );
    }
    await connection.commit();
    res.json({ message: poll.allow_vote_change && existing.length ? 'Vote updated successfully' : 'Vote submitted successfully' });
  } catch (error) {
    await connection.rollback();
    console.error('Cast notice vote error:', error);
    res.status(500).json({ message: error.message || 'Server error' });
  } finally {
    connection.release();
  }
};

const getNoticeStats = async (req, res) => {
  try {
    await ensurePollSchema();
    await sendPollTimelineNotifications();
    const [rows] = await promisePool.query(`
      SELECT
        (SELECT COUNT(*)::int FROM notices) AS total_notices,
        (SELECT COUNT(*)::int FROM notice_polls) AS notices_with_polls,
        (SELECT COUNT(*)::int FROM notice_polls WHERE closed_at IS NULL AND NOW() BETWEEN start_at AND end_at) AS active_polls,
        (SELECT COUNT(*)::int FROM notice_polls WHERE closed_at IS NOT NULL OR NOW() > end_at) AS closed_polls,
        (SELECT COUNT(DISTINCT (poll_id, resident_id))::int FROM notice_poll_votes) AS total_votes
    `);
    const eligible = await eligibleResidentCount();
    const stats = rows[0] || {};
    const pollCount = Number(stats.notices_with_polls || 0);
    const totalVotes = Number(stats.total_votes || 0);
    res.json({
      ...stats,
      total_notices: Number(stats.total_notices || 0),
      notices_with_polls: pollCount,
      active_polls: Number(stats.active_polls || 0),
      closed_polls: Number(stats.closed_polls || 0),
      total_votes: totalVotes,
      participation_percent: eligible && pollCount ? Math.round((totalVotes / (eligible * pollCount)) * 100) : 0
    });
  } catch (error) {
    console.error('Notice stats error:', error);
    res.status(500).json({ message: 'Server error' });
  }
};

