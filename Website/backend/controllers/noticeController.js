const createNotice = async (req, res) => {
  const connection = await promisePool.getConnection();
  try {
    await ensurePollSchema();
    const { title, description, poll } = req.body;
    if (!title || !description) return res.status(400).json({ message: 'Title and description are required' });

    await connection.beginTransaction();
    const [result] = await connection.query(
      'INSERT INTO notices (title, description, status) VALUES (?, ?, ?)',
      [title, description, 'published']
    );
    await savePoll(connection, result.insertId, poll);
    await connection.commit();

    await notifyResidents(
      poll?.enabled ? `New notice with poll: ${title}` : title,
      poll?.enabled ? (poll.question || 'A new poll is available in the notice board.') : (description || 'New society notice'),
      result.insertId
    );

    res.status(201).json({ id: result.insertId, title, description });
  } catch (error) {
    await connection.rollback();
    console.error('Create notice error:', error);
    res.status(500).json({ message: error.message || 'Server error' });
  } finally {
    connection.release();
  }
};

const updateNotice = async (req, res) => {
  const connection = await promisePool.getConnection();
  try {
    await ensurePollSchema();
    const { id } = req.params;
    const { title, description, poll } = req.body;
    if (!title || !description) return res.status(400).json({ message: 'Title and description are required' });

    await connection.beginTransaction();
    const [updated] = await connection.query(
      'UPDATE notices SET title = ?, description = ?, updated_at = NOW() WHERE id = ?',
      [title, description, id]
    );
    if (!updated.affectedRows) {
      await connection.rollback();
      return res.status(404).json({ message: 'Notice not found' });
    }
    await savePoll(connection, id, poll);
    await connection.commit();
    res.json({ message: 'Notice updated successfully' });
  } catch (error) {
    await connection.rollback();
    console.error('Update notice error:', error);
    res.status(500).json({ message: error.message || 'Server error' });
  } finally {
    connection.release();
  }
};

