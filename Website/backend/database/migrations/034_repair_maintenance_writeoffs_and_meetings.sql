-- Production safety repair for Android parity.
-- This migration is intentionally idempotent and only creates missing schema
-- used by maintenance write-offs and meeting management. It does not delete or
-- rewrite existing production data.

ALTER TABLE maintenance
  ADD COLUMN IF NOT EXISTS original_amount NUMERIC(12, 2),
  ADD COLUMN IF NOT EXISTS write_off_amount NUMERIC(12, 2) NOT NULL DEFAULT 0,
  ADD COLUMN IF NOT EXISTS remaining_due NUMERIC(12, 2),
  ADD COLUMN IF NOT EXISTS current_due NUMERIC(12, 2);

UPDATE maintenance
SET
  original_amount = COALESCE(original_amount, amount, total_amount, 0),
  write_off_amount = COALESCE(write_off_amount, 0),
  remaining_due = COALESCE(remaining_due, remaining_amount, GREATEST(COALESCE(total_amount, amount, 0) - COALESCE(paid_amount, 0) - COALESCE(write_off_amount, 0), 0)),
  current_due = COALESCE(current_due, remaining_amount, GREATEST(COALESCE(total_amount, amount, 0) - COALESCE(paid_amount, 0) - COALESCE(write_off_amount, 0), 0))
WHERE original_amount IS NULL
   OR remaining_due IS NULL
   OR current_due IS NULL
   OR write_off_amount IS NULL;

ALTER TABLE maintenance
  DROP CONSTRAINT IF EXISTS maintenance_status_check;

ALTER TABLE maintenance
  ADD CONSTRAINT maintenance_status_check
  CHECK (status IN (
    'Draft', 'Generated', 'Pending', 'Partial', 'Partially Paid', 'Paid', 'Overdue',
    'Under Review', 'Pending Verification', 'Rejected', 'Waived', 'Cancelled',
    'pending', 'paid',
    'UNPAID', 'PARTIALLY_PAID', 'PAID', 'PARTIAL_WRITE_OFF', 'WRITTEN_OFF', 'SETTLED'
  ));

